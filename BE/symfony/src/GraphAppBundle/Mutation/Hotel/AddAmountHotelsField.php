namespace GraphAppBundle\Mutation\Hotel;


use Doctrine\ORM\EntityManager;
use GraphAppBundle\Entity\Hotel\Hotel;
use GraphAppBundle\Entity\Hotel\HotelType;
use Youshido\GraphQL\Config\Field\FieldConfig;
use Youshido\GraphQL\Execution\ResolveInfo;
use Youshido\GraphQL\Type\ListType\ListType;
use Youshido\GraphQL\Type\NonNullType;
use Youshido\GraphQL\Type\Scalar\IntType;
use Youshido\GraphQLBundle\Field\AbstractContainerAwareField;

class AddAmountHotelsField extends AbstractContainerAwareField
{
    public function build(FieldConfig $config)
    {
        $config->addArguments([
            'amount' => new NonNullType(new IntType())
        ]);
    }

    public function resolve($value, array $args, ResolveInfo $info)
    {
        /** @var EntityManager $em */
        $em = $this->container->get('doctrine')->getManager();
        $hotelRepository = $em->getRepository(Hotel::class);

        $amount = $args['amount'];
        foreach (range(1, $amount) as $n) {
            $hotel = new Hotel();
            $hotel->setName('AUTOGENERATED-' . $n);

            // tells Doctrine you want to (eventually) save the Product (no queries yet)
            $em->persist($hotel);

            // actually executes the queries (i.e. the INSERT query)
            $em->flush();
        }

        return $hotelRepository->findAll();
    }

    /**
     * @return ListType
     */
    public function getType()
    {
        return new ListType(new HotelType());
    }
}